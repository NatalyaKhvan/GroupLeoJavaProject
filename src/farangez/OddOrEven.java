package farangez;

public class OddOrEven {
    public static void main(String[] args) {
        oddOrEven(3);
    }

        public static void oddOrEven(int num) {

            if (num % 2 == 0) {
                System.out.println("Number is even");
            } else {
                System.out.println("Number is odd");
            }

        }

}
